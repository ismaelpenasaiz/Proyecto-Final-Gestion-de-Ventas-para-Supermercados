package com.example.Gestion.de.Ventas.para.Supermercados.config;

import io.swagger.v3.oas.models.OpenAPI;
import io.swagger.v3.oas.models.info.Info;
import io.swagger.v3.oas.models.info.Contact;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

@Configuration
public class OpenApiConfig {

    @Bean
    public OpenAPI customOpenAPI() {
        return new OpenAPI()
                .info(new Info()
                        .title("API Gestión de Ventas - Supermercados")
                        .version("1.0")
                        .description("API RESTful para la gestión de ventas en supermercados. " +
                                "Permite administrar productos, sucursales y registrar ventas con sus detalles.")
                        .contact(new Contact()
                                .name("Equipo de Desarrollo")
                                .email("contacto@supermercado.com")));
    }
}